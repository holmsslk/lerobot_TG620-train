import time
import numpy as np

from arm import Arm
import rclpy

def main():
    rclpy.init(args=None)
    self_arm = Arm()
    array = np.array([1.0,1.0], dtype=np.float64)
    self_arm.SetMotionProfile(array) 
    time.sleep(3)
    array = np.array([169, 0, 0, 0, 0, 0], dtype=np.float64)
    self_arm.MoveWithJointAngle(array)
    time.sleep(3)
    self_arm.JOINTPrint()
    array = np.array([-169, 0, 0, 0, 0, 0], dtype=np.float64)
    self_arm.MoveWithJointAngle(array)
    time.sleep(3)
    self_arm.JOINTPrint()
    self_arm.ResetArm()
    time.sleep(3)
    self_arm.JOINTPrint()
