    self_arm.MoveWithJointAngle(array)
    time.sleep(3)
    self_arm.JOINTPrint()
    self_arm.ResetArm()
    time.sleep(3)
    self_arm.JOINTPrint()
    array = np.array([0, 0, 89, 0, 0, 0], dtype=np.float64)
    self_arm.MoveWithJointAngle(array)
    time.sleep(3)
    self_arm.JOINTPrint()
    array = np.array([0, 0, -89, 0, 0, 0], dtype=np.float64)
